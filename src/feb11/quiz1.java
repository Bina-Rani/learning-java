package feb11;

public class quiz1 {
    // print squares of 5 to 10

    public static void main(String args[]){
        int a=5;
        while(a<=10){
            System.out.println(a*a);
            a++;
        }
        for(a=5; a<=10; a++){
            System.out.println(a*a);
        }
    }
}
