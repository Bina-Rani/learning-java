package feb11;

public class quiz3 {
    //write a program to print a multiplication table of 7

    public static void main(String args[]){
        int a=1;
        while(a<=10){
            System.out.println(a*7);
            a++;
        }
        for(a=1; a<=10; a++){
            System.out.println(a*7);
        }

    }
}
